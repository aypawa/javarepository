package polymorphism;

public class Overloading_Study {

    public void add(int a, int b)
    {
    	int sum=a+b;
    	System.out.println("Sum of a and b is "+sum);
    }
		
    public void add(int a, int b,int c)
    {
    	int sum=a+b+c;
    	System.out.println("Sum of a, b & c is "+sum);
    }
		
		
		
	}


